package hulubrothers;

public class Point {
    private int x;
    private int y;
    Point ()
    {
        x=0;
        y=0;
    }
    public void setX(int input)
    {
        x=input;
    }
    public void setY(int input)
    {
        y=input;
    }
    public int X()
    {
        return x;
    }
    public int Y()
    {
        return y;
    }
}
